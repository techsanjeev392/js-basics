//Operators in js
//Arithmetic Operators
let a = 10;
let b = 4;
console.log("a+b=", a + b);
console.log("a-b=", a - b);
console.log("a/b=", a / b);
console.log("a**b=", a ** b);
console.log("a%b=", a % b);

//Assignment Operators
let assignment = 2;
// assignment += 5;
assignment **= 6;
console.log(assignment);

//comparison operators
comp1 = 5;
comp2 = 5;
comp3 = "5";

if (comp1 == comp2) {
  console.log("comp1 is equal to comp2");
} else {
  console.log("comp1 is not equal to comp2");
}
console.log(comp1 === comp2);

//Logical Operators
if (comp1 == comp2 && comp1 == comp3)
  console.log("comp1 is equal to comp2 and comp3");
else console.log("comp1 is not equal to comp2 and comp3");
